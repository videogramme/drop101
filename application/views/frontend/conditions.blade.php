<p>
Mentions légales en Belgique
Par Philippe Gueuning, vendredi 16 mars 2007 à 10:34	:: Votre site ? Votre image !
</p>
<p>
Il ne vous viendrait pas à l'esprit d'envoyer des factures sans certains renseignements obligatoires comme la TVA, la référence de la facture, le taux de TVA... Mais savez-vous que, si vous possédez un site Web, vous devez également afficher certains nombre de données ?
</p>
<p>
Ces dernières ont été fixées par la loi belge sur le commerce électronique, que votre site soit un simple site vitrine ou qu'il prévoie de la vente en ligne. Sans respect de ces obligations, vos visiteurs pourraient résilier tout contrat conclu avec vous !
</p>
<p>
Voici donc ce que vous vous devez d'afficher sur vos pages :
</p>
<p>
Nom personnel ou nom commercial
Numéro d'entreprise
Adresse postale complète
Coordonnées complètes permettant de vous joindre rapidement et directement : téléphone, fax, gsm, E-Mail
Coordonnées des autorités de contrôle si votre activité est soumise à un régime d'autorisation
Coordonnées de l'association professionnelle, titre professionnel et état de délivrance de ce dernier si votre profession est réglementée
Règles de conduite si existantes
En cas de catalogue avec affichage de prix, ces prix doivent être exprimés de manière claire et et non équivoque (avec ou sans impôts, taxes, frais de livraison...)
</p>
